// core/src/eval/report-card/render.ts
import type { ReportCard } from "./types.js";
import { CONTRACT_FINDINGS } from "./taxonomy.js";

const pct = (x: number) => `${Math.round(x * 100)}%`;
const s = (ms: number) => `${(ms / 1000).toFixed(1)}s`;

export function renderReportCardMarkdown(card: ReportCard, provenance?: string): string {
  const fa = card.fieldAccuracy;
  const out: string[] = [];
  out.push("# context.dev Extraction Report Card", "");
  out.push("> Reproducible, honest evaluation of how well context.dev extracts structured company profiles. Generated by `core/src/eval/report-card`.", "");
  if (provenance) out.push(`> **Data source:** ${provenance}`, "");
  out.push("## Summary", "");
  out.push("| Metric | Value |", "| --- | --- |");
  out.push(`| Corpus size | ${card.corpusSize} players |`);
  out.push(`| Profiled | ${card.gradedCount} / ${card.corpusSize} |`);
  out.push(`| Failed | ${card.failedCount} |`);
  out.push(`| **Headline score** | **${pct(card.headlineScore)}** fields captured |`);
  out.push(`| Mean latency | ${s(card.latencyMs.mean)} (p50 ${s(card.latencyMs.p50)}, p95 ${s(card.latencyMs.p95)}) |`);
  out.push(`| Credits / page | ${card.creditsPerPage} (total ${card.totalCredits}) |`, "");
  out.push("## Per-field accuracy", "");
  out.push("| Field | Accuracy |", "| --- | --- |");
  out.push(`| Name (exact) | ${pct(fa.name)} |`);
  out.push(`| One-liner (token recall) | ${pct(fa.oneLiner)} |`);
  out.push(`| Positioning (token recall) | ${pct(fa.positioning)} |`);
  out.push(`| Tags (F1) | ${pct(fa.tagsF1)} |`);
  out.push(`| Features (fuzzy F1) | ${pct(fa.featuresF1)} |`);
  out.push(`| Links (host match) | ${pct(fa.linksCorrect)} |`, "");
  out.push("## Failure taxonomy", "");
  if (card.failures.length === 0) {
    out.push("_No extraction failures in this corpus run._", "");
  } else {
    out.push("| Category | Count |", "| --- | --- |");
    for (const f of card.failures) out.push(`| \`${f.category}\` | ${f.count} |`);
    out.push("");
  }
  out.push("### Historical contract findings (found & fixed)", "");
  out.push("API-contract issues surfaced while integrating context.dev, and how we resolved them:", "");
  for (const f of CONTRACT_FINDINGS) out.push(`- **${f.title}** (\`${f.id}\`) — ${f.found} _Fix:_ ${f.fix}`);
  out.push("");
  out.push("## Methodology", "");
  out.push("Grading is offline and deterministic against a committed corpus of hand-checked ground-truth profiles. The live `--collect` step calls context.dev to refresh the recorded extractions, timing each call and reading credits from the ledger; grading then runs with no key. Name uses normalized exact match; one-liner/positioning use ground-truth token recall (hit ≥ 0.5); tags use exact-set F1; features use fuzzy (token-overlap) F1; links match on host. Numbers are reported as measured — no cherry-picking.", "");
  return out.join("\n");
}
